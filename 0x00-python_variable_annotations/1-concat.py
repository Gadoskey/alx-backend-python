#!/usr/bin/env python3

"""
Simple Addition Function

Author: Yusuf Mustapha Opeyemi

This function takes two floating-point numbers as input and returns their sum.

Parameters:
  num1: The first floating-point number.
  num2: The second floating-point number.

Returns: The sum of `num1` and `num2`.
"""

def add(num1: float, num2: float) -> float:
    # Adds num1 and num2 together and returns the result of the addition
    return num1 + num2
